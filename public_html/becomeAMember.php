				<p>At the beginning of the season, selectors are chosen by the Central Region for each grade. Nearing the end of the league season the selectors meet and decide who will be sent an invitation to trial for the Inter Regional team. Successful candidates from the trials will compete against the three other regions at the Inter Regional Championships – These championships are held over Labour Weekend.</p>

