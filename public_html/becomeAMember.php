				<p>The hockey season lasts from March through until the end of October. During the months of March through August teams compete against each other within their own region. In our case this includes Hamilton, Morrinsville, Whakatane, Tauranga, New Plymouth, Taupo and Waihi. There are four venues currently available for League games in the Central Region; these are Hamilton, Waihi, Tauranga and New Plymouth.
				During the season teams are expected to play on a home and away basis. League games are normally played every weekend with the exception of school holidays, and are played on either a Saturday or a Sunday. Teams work together to make sure all players are at games, with parents taking turns at travelling to “away” games.</p>

				<h3>National Championships</h3>
				<p>The 2 top club teams in each grade from the Regional League or Regional Playoffs go through to the National Championships –These are normally held in the September school holidays.</p>

